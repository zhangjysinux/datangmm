QString ChineseToPinyin::getPinyin(const QString& str)
{
    QList<QString> pinyin_list;
    QString pinyinStr;
    for(int i=0; i<str.count(); i++)
    {
        QString longgerPinyins;
        QChar c = str.at(i);
        ushort u = c.unicode();
        if(u >= 0x4E00 && u <= 0x9FA5)
        {
            //this is chinese
            QString longgerPinyin;
            pinyin_list = m_pinyin_map.values(str.at(i));
            foreach(const QString &pinyin, pinyin_list)
            {
                if(longgerPinyin.count() < pinyin.count()){
                    longgerPinyin = pinyin;
                }
            }
            foreach(const QString &pinyin, pinyin_list)
            {
                if(longgerPinyin.count() == pinyin.count()){
                    longgerPinyins.push_back(QString("%1 ").arg(pinyin));
                }
            }
            if(!longgerPinyins.isEmpty())
                longgerPinyins = longgerPinyins.trimmed();
        }
        pinyinStr.push_back(longgerPinyins.isEmpty()? c : longgerPinyins);
    }
    return pinyinStr.toUpper();
}

