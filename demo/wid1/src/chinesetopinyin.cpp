static char pinyinFirstLetter(unsigned short hanzi)
{
    int index = hanzi - HANZI_START;
    if (index >= 0 && index <= HANZI_COUNT)
    {
        return firstLetterArray[index];
    }
    else
    {
        return hanzi;
    }
}

//get the str chinese pinyi first letter as return;
QString ChineseToPinyin::getPinyinFirstLetter(const QString& str)
{
    QString convertString;
    ushort u;
    for(int i = 0, count = str.count(); i < count; i++)
    {
        QChar c1 = str.at(i);
        u = c1.toLower().unicode();
        if(u >= 0x4E00 && u <= 0x9FA5)
        {
            //this is chinese
            u = pinyinFirstLetter(u);
        }
        convertString.push_back(QChar::fromLatin1(u));
    }
    return convertString.toUpper();
}
