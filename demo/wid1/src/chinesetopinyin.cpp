ChineseToPinyin::ChineseToPinyin()
{
    QRegExp regExp;
    QFile pinyin_file;

    pinyin_file.setFileName(":/pinyingbknotongyin");

    if( !pinyin_file.open(QIODevice::ReadOnly) )
        qDebug() << "error: can not open pinyin";

    regExp.setCaseSensitivity(Qt::CaseSensitive);
    regExp.setPattern(QString("([a-z]+)"));

    QTextCodec *code = QTextCodec::codecForName("GB18030");

    while(!pinyin_file.atEnd())
    {
        QString data = code->toUnicode(pinyin_file.readLine().data());
        data = data.remove(".");
        data = data.trimmed();
        int ret = regExp.indexIn(data,0,QRegExp::CaretAtZero);
        QString cap1 = regExp.cap(1);
        QString cap2 = data.left(ret);
        m_pinyin_map.insert(cap2, cap1);
    }
}

